#include "ft_printf.h"

void	prt_ilist(t_mylst **lst)
{
	t_mylst	*l;
	int		i;

	i = 1;
	l = *lst;
	if (l)
	{
		while (l != NULL)
		{
			l->i = i;
			l = l->next;
			++i;
		}
	}
}
